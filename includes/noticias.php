                    <h4>Breve historia de CIMPRE</h4>
                    <h6>CIMPRE, Centro Integral de Medicina preventiva y de Salud Ocupacional</h6>, abrió sus puertas en 1989, como una empresa innovadora y pionera en la prestación de servicios de medicina asistencial preventiva y asesorando en los aspectos de promoción y prevención de la Salud Ocupacional.
Durante veinte años, CIMPRE, se ha posicionado como aliada de las empresas, no sólo en el aseguramiento a sus necesidades de salud, sino en la promoción y prevención de accidentes y enfermedades profesionales mediante sus servicios de Salud Ocupacional, de esta manera aumentando la productividad de las empresas quienes ahora pueden delegar este tema.
CIMPRE, ha abierto una nueva sede en MAMONAL, en BLOC PORT, para estar más cerca de este importante sector industrial y poder desarrollar planes integrales de Salud Ocupacional con respuesta rápida y con impacto positivo en los resultados. CIMPRE pretende servir a todas sus empresas afiliadas posicionándose entre ellos, como una marca de servicios médicos especializados, para ser un aliado estratégico, en el propósito de mantener, empresas, familias y trabajadores seguros, sanos y productivos en sus actividades.
